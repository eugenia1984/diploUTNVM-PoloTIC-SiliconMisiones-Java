package miproyecto;

public class PaisDeOrigen {
    //atributos
    private String idioma;
    private String nombre;
    
    //geter y setters
    public String getIdioma() {
        return idioma;
    }

    public void setIdioma(String idioma) {
        this.idioma = idioma;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }
}
